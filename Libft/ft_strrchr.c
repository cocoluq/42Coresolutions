#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		while (c == s[i])
		{
			i++;
		}
		return ((char *)s + i);
	}
	return (NULL);
}
